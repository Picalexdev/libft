#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	int		x;

	if (!s || !f)
		return ;
	x = 0;
	while (s[x])
	{
		f(x, &s[x]);
		x++;
	}
}
