#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*dst;

	if (lst)
	{
		if (!*lst)
			*lst = new;
		else
		{
			dst = ft_lstlast;
		}
	}
}
